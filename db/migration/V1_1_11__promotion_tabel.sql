CREATE TABLE IF NOT EXISTS tcpds_2_1_0.promotion (
p_promo_sk integer NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
p_promo_id char(16) NOT NULL,
p_start_date_sk integer,
p_end_date_sk integer,
p_item_sk integer,
p_cost decimal(15,2),
p_response_target integer,
p_promo_name char(50),
p_channel_dmail char(1),
p_channel_email char(1),
p_channel_catalog char(1),
p_channel_tv char(1),
p_channel_radio char(1),
p_channel_press char(1),
p_channel_event char(1),
p_channel_demo char(1),
p_channel_details varchar(100),
p_purpose char(15),
p_discount_active char(1),
CONSTRAINT fk_p_start_date_sk FOREIGN KEY(p_start_date_sk) REFERENCES tcpds_2_1_0.date_dim(d_date_sk),
CONSTRAINT fk_p_end_date_sk FOREIGN KEY(p_end_date_sk) REFERENCES tcpds_2_1_0.date_dim(d_date_sk),
CONSTRAINT fk_p_item_sk FOREIGN KEY(p_item_sk) REFERENCES tcpds_2_1_0.item(i_item_sk)
)